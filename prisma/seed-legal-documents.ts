async function seedLegalDocuments() {
  console.log('利用規約・プライバシーポリシーの初期データ登録を開始...');

  const documents = [
    { docType: 'TERMS' as const, targetType: 'WORKER' as const, content: workerTermsContent, label: 'ワーカー利用規約' },
    { docType: 'TERMS' as const, targetType: 'FACILITY' as const, content: facilityTermsContent, label: '施設利用規約' },
    { docType: 'PRIVACY' as const, targetType: 'WORKER' as const, content: privacyContent, label: 'プライバシーポリシー' },
  ];

  for (const doc of documents) {
    // 既にデータが存在するかチェック
    const existing = await prisma.legalDocument.findFirst({
      where: {
        doc_type: doc.docType,
        target_type: doc.targetType,
        is_current: true,
      },
    });

    if (existing) {
      console.log(`  [スキップ] ${doc.label}: 既にデータが存在します (id=${existing.id}, version=${existing.version})`);
      continue;
    }

    // 新規登録
    const created = await prisma.legalDocument.create({
      data: {
        doc_type: doc.docType,
        target_type: doc.targetType,
        content: doc.content,
        version: 1,
        is_current: true,
        published_at: new Date(),
        created_by: 1, // SystemAdmin ID = 1
      },
    });

    console.log(`  [作成] ${doc.label}: id=${created.id}, version=${created.version}`);
  }

  console.log('完了');
}

