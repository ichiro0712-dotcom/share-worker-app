// 今日の日付を基準にする
const today = new Date();
const formatDate = (daysFromToday: number) => {
  const date = new Date(today);
  date.setDate(today.getDate() + daysFromToday);
  return date.toISOString().split('T')[0];
};

// 締切時刻を生成（勤務日の前日17時）
const generateDeadline = (workDate: string): string => {
  const date = new Date(workDate);
  date.setDate(date.getDate() - 1);
  date.setHours(17, 0, 0, 0);
  return date.toISOString();
};

// 日給を計算
const calculateWage = (startTime: string, endTime: string, breakTime: number, hourlyWage: number, transportationFee: number): number => {
  const start = parseInt(startTime.split(':')[0]) + parseInt(startTime.split(':')[1]) / 60;
  const end = parseInt(endTime.split(':')[0]) + parseInt(endTime.split(':')[1]) / 60;
  const workHours = end - start - breakTime / 60;
  return Math.round(workHours * hourlyWage + transportationFee);
};

