/**
 * 値が指定の旧値リストに含まれているかを判定する。
 * マイページの<select>に「（旧）xxx」を条件付き表示するために使用。
 */
export function isLegacyOption(
  value: string | null | undefined,
  legacyOptions: ReadonlyArray<string>
): boolean {
  if (!value) return false;
  return legacyOptions.includes(value);
}
